package com.mycompany.tiposdedatos;


public class TiposdeDatos {

    public static void main(String[] args) {
        byte entero = 12;  // Definimos la variable
        System.out.println("Numero entero: "+entero);
      
        short entero2 = 12456;
        System.out.println("Otro número entero: "+entero2);
        
        long entero3 = 1000000;
        System.out.println("Y uno más: "+entero3);
        
        float decimal = 3.45f; // Para datos flotantes, es necesario ponerle una f
        System.out.println("Número decimal: "+decimal);
        
        char caracter = 'a';
        System.out.println("Caracter: "+caracter);
        
        boolean decision = true;
        System.out.println("La decisión es: "+decision);
        
    }
}
